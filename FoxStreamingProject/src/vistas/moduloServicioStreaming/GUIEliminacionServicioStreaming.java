    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        // TODO add your handling code here:
        try {
            if(servicioStreamingPorEliminar.getTipoServicioId() == 0){
                throw new DatoNoValido("Debe seleccionar una fila de la tabla.");
            }
            controlador.eliminarTipoServicio(servicioStreamingPorEliminar);

            JOptionPane.showMessageDialog(this, String.format("Datos de la plataforma %s eliminados correctamente.", servicioStreamingPorEliminar.getNombre()));

            establecerPorDefecto();
        } catch (DatoNoValido | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarMouseClicked

