    private void llenarJTablePlataformas(){
        DefaultTableModel dtm = new DefaultTableModel();
        
        ArrayList<TipoServicioStreaming> plataformas = new ArrayList<>();
        
        try {
            plataformas = controlador.obtenerServiciosStreaming();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Hubo un problema al cargar los datos.");
        }
        
        String[] cabecera = {"Código", "Nombre de la plataforma", "Límite de usuarios"}; 
        dtm.setColumnIdentifiers(cabecera);
        dtm.setColumnCount(cabecera.length);
        Object[] dataPlataformas = new Object[dtm.getColumnCount()];
        
        for (int i = 0; i < plataformas.size(); i++) {
            dataPlataformas[0] = plataformas.get(i).getTipoServicioId();
            dataPlataformas[1] = plataformas.get(i).getNombre();
            dataPlataformas[2] = plataformas.get(i).getMaxUsuarios();
            dtm.addRow(dataPlataformas);
        }
        jTablePlataformas.setModel(dtm);
    }
    
