    private void btnActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarMouseClicked
        // TODO add your handling code here:
        try {
            if(lblCodigo.getText().equals("")){
                throw new DatoNoValido("Debe seleccionar una fila de la tabla.");
            }
            
            TipoServicioStreaming objTipoServicioStreaming = new TipoServicioStreaming();

            objTipoServicioStreaming.setTipoServicioId(Integer.parseInt(lblCodigo.getText()));
            objTipoServicioStreaming.setNombre(txtNombre.getText().trim());
            objTipoServicioStreaming.setMaxUsuarios(Integer.parseInt(txtCantMaxUsuarios.getText().trim()));

            controlador.actualizarTipoServicio(objTipoServicioStreaming);

            JOptionPane.showMessageDialog(this, String.format("Datos del servicio streaming %s actualizados correctamente.", objTipoServicioStreaming.getNombre()));

            establecerPorDefecto();
        } catch (DatoNoValido | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error. Intente nuevamente.");
        }
    }//GEN-LAST:event_btnActualizarMouseClicked

