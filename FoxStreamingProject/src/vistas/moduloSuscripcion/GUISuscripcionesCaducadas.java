    private void llenarJTableSuscripciones(){
        DefaultTableModel dtm = new DefaultTableModel();
        
         ControladorSuscripcion controlador = new ControladorSuscripcion();
        ArrayList<Suscripcion> suscripciones = new ArrayList<>();
        try {
            suscripciones = controlador.obtenerSuscripcionesCaducadas();
        } catch (SQLException ex) {
            Logger.getLogger(GUISuscripcionesCaducadas.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String[] cabecera = {"ID Suscripcion", "Cliente", "Plataforma", "Correo cuenta", "Fecha inicio", "Vencimiento"}; 
        dtm.setColumnIdentifiers(cabecera);
        dtm.setColumnCount(cabecera.length);
        Object[] dataSuscripciones = new Object[dtm.getColumnCount()];
        
        for (int i = 0; i < suscripciones.size(); i++) {
            dataSuscripciones[0] = suscripciones.get(i).getSucripcionID();
            dataSuscripciones[1] = suscripciones.get(i).getNombreCliente();
            dataSuscripciones[2] = suscripciones.get(i).getPlataformaStreamingAsociada();
            dataSuscripciones[3] = suscripciones.get(i).getCorreoCuentaAsociada();
            dataSuscripciones[4] = suscripciones.get(i).getFechaInicio();
            dataSuscripciones[5] = suscripciones.get(i).getFechaFin();
            dtm.addRow(dataSuscripciones);
        }
        jTableSuscripciones.setModel(dtm);
    }
    
    private void establecerPorDefecto(){
        //cargarCodigo();
        llenarJTableSuscripciones();
    }
    
