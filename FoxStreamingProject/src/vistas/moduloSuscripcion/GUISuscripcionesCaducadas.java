    private void btnProcesarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnProcesarMouseClicked
        // TODO add your handling code here:
        try {
            
            ControladorSuscripcion controlador = new ControladorSuscripcion();
            ArrayList<Suscripcion> suscripciones = controlador.obtenerSuscripcionesCaducadas();
            
            for(int i = 0; i < suscripciones.size(); i++){
                controlador.actualizarEstadoSuscripcion(suscripciones.get(i).getSucripcionID());
            }
            
            JOptionPane.showMessageDialog(this, "Se han realizado cambios en el sistema.");
            
            establecerPorDefecto();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un número ENTERO.");
        }
    }//GEN-LAST:event_btnProcesarMouseClicked

